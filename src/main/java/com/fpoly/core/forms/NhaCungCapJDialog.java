void search() {
    // Lấy từ khóa tìm kiếm từ trường nhập liệu
    String keyword = txtseach.getText();

    // Kiểm tra xem từ khóa có bị bỏ trống hay không
    if (keyword.isEmpty()) {
        MsgBox.alert(this, "Vui lòng nhập từ khóa để tìm kiếm!");
        return;
    }

    // Gọi phương thức tìm kiếm từ lớp NhaCungCapDAO
    try {
        List<NhaCungCap> list = dao.searchByKeyword(keyword);
        fillToTablesearch(list); // Cập nhật lại bảng với danh sách kết quả tìm kiếm
    } catch (Exception e) {
        e.printStackTrace();
        MsgBox.alert(this, "Lỗi khi tìm kiếm nhà cung cấp!");
    }
}

private void fillToTablesearch(List<NhaCungCap> list) {
    DefaultTableModel model = (DefaultTableModel) banghienthiphieumuon.getModel();
    model.setRowCount(0);
    for (NhaCungCap ncc : list) {
        Object[] row = {
            ncc.getMaNCC(),
            ncc.getTenNCC(),
            ncc.getDiaChi(),
            ncc.getSDT()
        };
        model.addRow(row);
    }
}

