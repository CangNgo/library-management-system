    private void fillToTable() {
    DefaultTableModel model = (DefaultTableModel) banghienthiphieumuon.getModel();
    model.setRowCount(0); // Xóa hết các dòng trong bảng hiện tại

    // Gọi phương thức selectAll để lấy danh sách nhà cung cấp từ CSDL
    List<NhaCungCap> list = selectAll(); // Sử dụng phương thức selectAll để lấy danh sách

    if (list != null) {
        for (NhaCungCap ncc : list) {
            Object[] row = {
                ncc.getMaNCC(),
                ncc.getTenNCC(),
                ncc.getSDT(),
                ncc.getDiaChi()
            };
            model.addRow(row); // Thêm một dòng vào bảng với dữ liệu của nhà cung cấp ncc
        }
    } else {
        MsgBox.alert(this, "Không có dữ liệu để hiển thị");
    }
}
    
    private void display(NhaCungCap ncc) {
        txtmancc.setText(ncc.getMaNCC());
        txttenncc.setText(ncc.getTenNCC());
        txtdiachi.setText(ncc.getDiaChi());
        txtsdt.setText(ncc.getSDT());
}

void edit() {
    String maNCC = (String) banghienthiphieumuon.getValueAt(this.row, 0);
    NhaCungCap ncc = dao.selectById(maNCC);
    if (ncc != null) {
        display(ncc);
    }
}

void insert() {
    String maNCC = txtmancc.getText();
    String tenNCC = txttenncc.getText();
    String diaChi = txtdiachi.getText();
    String sdt = txtsdt.getText();

    // Kiểm tra xem các trường có bị bỏ trống hay không
    if (maNCC.isEmpty() || tenNCC.isEmpty() || diaChi.isEmpty() || sdt.isEmpty()) {
        MsgBox.alert(this, "Vui lòng điền đầy đủ thông tin!");
        return;
    }

    // Tạo đối tượng NhaCungCap mới
    NhaCungCap ncc = new NhaCungCap();
    ncc.setMaNCC(maNCC);
    ncc.setTenNCC(tenNCC);
    ncc.setDiaChi(diaChi);
    ncc.setSDT(sdt);

    // Gọi phương thức insert từ lớp NhaCungCapDAO
    try {
        dao.insert(ncc);
        MsgBox.alert(this, "Thêm nhà cung cấp thành công!");
        fillToTable(); // Cập nhật lại bảng
    } catch (Exception e) {
        e.printStackTrace();
        MsgBox.alert(this, "Lỗi khi thêm nhà cung cấp!");
    }
}

void update() {
    String maNCC = txtmancc.getText();
    String tenNCC = txttenncc.getText();
    String diaChi = txtdiachi.getText();
    String sdt = txtsdt.getText();

    // Kiểm tra xem các trường có bị bỏ trống hay không
    if (maNCC.isEmpty() || tenNCC.isEmpty() || diaChi.isEmpty() || sdt.isEmpty()) {
        MsgBox.alert(this, "Vui lòng điền đầy đủ thông tin!");
        return;
    }

    // Tạo đối tượng NhaCungCap mới với thông tin đã cập nhật
    NhaCungCap ncc = new NhaCungCap();
    ncc.setMaNCC(maNCC);
    ncc.setTenNCC(tenNCC);
    ncc.setDiaChi(diaChi);
    ncc.setSDT(sdt);

    // Gọi phương thức update từ lớp NhaCungCapDAO
    try {
        dao.update(ncc);
        MsgBox.alert(this, "Sửa thông tin nhà cung cấp thành công!");
        fillToTable(); // Cập nhật lại bảng
    } catch (Exception e) {
        e.printStackTrace();
        MsgBox.alert(this, "Lỗi khi sửa thông tin nhà cung cấp!");
    }
}

void delete() {
    String maNCC = txtmancc.getText();
    // Kiểm tra xem mã nhà cung cấp có bị bỏ trống hay không
    if (maNCC.isEmpty()) {
        MsgBox.alert(this, "Vui lòng nhập mã nhà cung cấp để xóa!");
        return;
    }
    // Xác nhận lại việc xóa
    int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa nhà cung cấp này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }

    // Gọi phương thức delete từ lớp NhaCungCapDAO
    try {
        dao.delete(maNCC);
        MsgBox.alert(this, "Xóa nhà cung cấp thành công!");
        fillToTable(); // Cập nhật lại bảng
    } catch (Exception e) {
        e.printStackTrace();
        MsgBox.alert(this, "Lỗi khi xóa nhà cung cấp!");
    }
}

